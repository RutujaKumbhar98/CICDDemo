package QKTraining.CalcilatorAPIDemo;

public class Calculator 
{
		public int Addition(int num1,int num2)
		{
			int res=num1+num2;
			return res;
		}
		public int Subtraction(int num1,int num2)
		{
			int res=num1-num2;
			return res;
		}
		public int Multiplication(int num1,int num2)
		{
			int res=num1*num2;
			return res;
		}

}
